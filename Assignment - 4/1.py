import numpy as np;
arr = np.array([2,5,8,13,14]);
print("Original array is: ",arr,"\n");
arr1 = arr + 2;
print("Array after adding 2 to all elements is: ",arr1,"\n");
arr2 = arr * 3;
print("Array after multiplying 3 to all elements is: ",arr2,"\n");
arr3 = arr / 2;
print("Array after dividing 2 to all elements is: ",arr3,"\n");